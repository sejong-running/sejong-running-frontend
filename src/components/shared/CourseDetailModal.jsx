import React, { useState, useEffect } from "react";
import "./CourseDetailModal.css";
import KakaoMap from "../map/KakaoMap";
import ImageSlider from "./ImageSlider";
import { getCourseById, getCourseImages } from "../../services/coursesService";

const CourseDetailModal = ({
    course,
    isOpen,
    onClose,
    onFavorite,
    onViewMap,
}) => {
    const [courseData, setCourseData] = useState(null);
    const [courseImages, setCourseImages] = useState([]);
    const [loading, setLoading] = useState(false);
    const [currentViewIndex, setCurrentViewIndex] = useState(0); // 0: ÏßÄÎèÑ, 1~: Ïù¥ÎØ∏ÏßÄÎì§
    const [showImageSlider, setShowImageSlider] = useState(false);

    // ÏΩîÏä§ Îç∞Ïù¥ÌÑ∞ Î°úÎìú
    useEffect(() => {
        const loadCourseData = async () => {
            if (!course || !course.id) return;

            setLoading(true);
            // Î™®Îã¨Ïù¥ Ïó¥Î¶¥ ÎïåÎßàÎã§ ÏßÄÎèÑÎ°ú Ï¥àÍ∏∞Ìôî
            setCurrentViewIndex(0);

            try {
                const [courseResult, imagesResult] = await Promise.all([
                    getCourseById(course.id),
                    getCourseImages(course.id),
                ]);

                if (courseResult.error) {
                    console.error("ÏΩîÏä§ Îç∞Ïù¥ÌÑ∞ Î°úÎìú Ïã§Ìå®:", courseResult.error);
                } else {
                    setCourseData(courseResult.data);
                }

                if (imagesResult.error) {
                    console.error("Ïù¥ÎØ∏ÏßÄ Î°úÎìú Ïã§Ìå®:", imagesResult.error);
                } else {
                    setCourseImages(imagesResult.data);
                }
            } catch (err) {
                console.error("Îç∞Ïù¥ÌÑ∞ Î°úÎìú Ï§ë Ïò§Î•ò:", err);
            } finally {
                setLoading(false);
            }
        };

        loadCourseData();
    }, [course]);

    if (!isOpen || !course) return null;

    const handleOverlayClick = (e) => {
        if (e.target === e.currentTarget) {
            onClose();
        }
    };

    const handleFavoriteClick = () => {
        onFavorite(course.id);
    };

    const handleViewMapClick = () => {
        onViewMap(course);
    };

    const handleImageClick = () => {
        if (courseImages.length > 0) {
            setShowImageSlider(true);
        }
    };

    const handlePrevView = () => {
        if (currentViewIndex > 0) {
            setCurrentViewIndex(currentViewIndex - 1);
        }
    };

    const handleNextView = () => {
        if (currentViewIndex < courseImages.length) {
            setCurrentViewIndex(currentViewIndex + 1);
        }
    };

    const handleGoToMap = () => {
        setCurrentViewIndex(0);
    };

    // Ïã§Ï†ú GeoJSON Îç∞Ïù¥ÌÑ∞ ÎòêÎäî ÏÉòÌîå Îç∞Ïù¥ÌÑ∞ ÏÇ¨Ïö©
    const getGeoJsonData = () => {
        if (courseData?.geomJson) {
            return courseData.geomJson;
        }
    };

    // Í≤ΩÍ≥Ñ Îç∞Ïù¥ÌÑ∞ Í∞ÄÏ†∏Ïò§Í∏∞
    const getBounds = () => {
        if (
            courseData?.minLatitude &&
            courseData?.maxLatitude &&
            courseData?.minLongitude &&
            courseData?.maxLongitude
        ) {
            return {
                minLat: courseData.minLatitude,
                maxLat: courseData.maxLatitude,
                minLng: courseData.minLongitude,
                maxLng: courseData.maxLongitude,
            };
        }
        return null;
    };

    const totalViews = 1 + courseImages.length; // ÏßÄÎèÑ + Ïù¥ÎØ∏ÏßÄÎì§
    const currentImage =
        currentViewIndex > 0 ? courseImages[currentViewIndex - 1] : null;

    return (
        <div className="modal-overlay" onClick={handleOverlayClick}>
            <div className="modal-content">
                {/* Ìó§Îçî */}
                <div className="modal-header">
                    <button className="modal-close" onClick={onClose}>
                        ‚úï
                    </button>
                </div>

                {/* Î∑∞ Ï†ÑÌôò Ïª®ÌÖåÏù¥ÎÑà */}
                <div className="course-detail-modal__view-container">
                    {/* ÌòÑÏû¨ Î∑∞ ÌëúÏãú */}
                    {currentViewIndex === 0 ? (
                        <div className="course-detail-modal__map-container">
                            <KakaoMap
                                geomJson={getGeoJsonData()}
                                width="100%"
                                height="100%"
                                fitBoundsOnChange={true}
                                boundsPadding={0}
                                controllable={false}
                                bounds={getBounds()}
                                routeStyle={{
                                    strokeWeight: 6,
                                    strokeColor: "#3B82F6",
                                    strokeOpacity: 0.85,
                                    strokeStyle: "solid",
                                }}
                            />
                        </div>
                    ) : (
                        <div className="course-detail-modal__single-image-container">
                            {/* Î∏îÎü¨ Ï≤òÎ¶¨Îêú Î∞∞Í≤Ω Ïù¥ÎØ∏ÏßÄ */}
                            <div
                                className="image-background-blur"
                                style={{
                                    backgroundImage: `url(${currentImage?.url})`,
                                }}
                            ></div>
                            {/* Ï†ÑÏ≤¥ Ïù¥ÎØ∏ÏßÄ */}
                            <img
                                src={currentImage?.url}
                                alt={`ÏΩîÏä§ Ïù¥ÎØ∏ÏßÄ ${currentViewIndex}`}
                                className="course-single-image"
                                onClick={handleImageClick}
                            />
                        </div>
                    )}

                    {/* ÎÑ§ÎπÑÍ≤åÏù¥ÏÖò Î≤ÑÌäºÎì§ */}
                    {totalViews > 1 && (
                        <>
                            {/* Ïù¥Ï†Ñ Î≤ÑÌäº */}
                            {currentViewIndex > 0 && (
                                <button
                                    className="view-nav-button view-nav-button--prev"
                                    onClick={handlePrevView}
                                >
                                    <svg
                                        width="24"
                                        height="24"
                                        viewBox="0 0 24 24"
                                        fill="none"
                                    >
                                        <path
                                            d="M15 18L9 12L15 6"
                                            stroke="currentColor"
                                            strokeWidth="2"
                                            strokeLinecap="round"
                                            strokeLinejoin="round"
                                        />
                                    </svg>
                                </button>
                            )}

                            {/* Îã§Ïùå Î≤ÑÌäº */}
                            {currentViewIndex < courseImages.length && (
                                <button
                                    className="view-nav-button view-nav-button--next"
                                    onClick={handleNextView}
                                >
                                    <svg
                                        width="24"
                                        height="24"
                                        viewBox="0 0 24 24"
                                        fill="none"
                                    >
                                        <path
                                            d="M9 18L15 12L9 6"
                                            stroke="currentColor"
                                            strokeWidth="2"
                                            strokeLinecap="round"
                                            strokeLinejoin="round"
                                        />
                                    </svg>
                                </button>
                            )}

                            {/* Ïù∏ÎîîÏºÄÏù¥ÌÑ∞ */}
                            <div className="view-indicators">
                                <button
                                    className={`view-indicator ${
                                        currentViewIndex === 0 ? "active" : ""
                                    }`}
                                    onClick={handleGoToMap}
                                ></button>
                                {courseImages.map((image, index) => (
                                    <button
                                        key={index}
                                        className={`view-indicator ${
                                            currentViewIndex === index + 1
                                                ? "active"
                                                : ""
                                        }`}
                                        onClick={() =>
                                            setCurrentViewIndex(index + 1)
                                        }
                                    ></button>
                                ))}
                            </div>
                        </>
                    )}
                </div>

                {/* Ï†úÎ™© */}
                <div className="modal-title-section">
                    <h2 className="modal-title">{course.title}</h2>
                </div>

                {/* ÏöîÏïΩ ÌÜµÍ≥Ñ */}
                <div className="course-summary">
                    <div className="summary-item">
                        <span className="summary-icon">üìç</span>
                        <span className="summary-text">
                            {courseData?.distance
                                ? `${courseData.distance}km`
                                : course.distance}
                        </span>
                    </div>
                    <div className="summary-item">
                        <span className="summary-icon">‚ù§Ô∏è</span>
                        <span className="summary-text">
                            {courseData?.likes_count || course.likes || "0"}
                        </span>
                    </div>
                </div>

                {/* ÏΩîÏä§ Ïú†Ìòï */}
                <div className="course-section">
                    <div className="section-header">
                        <h3 className="section-title">ÏΩîÏä§ Ïú†Ìòï</h3>
                    </div>
                    <div className="section-content">
                        <div className="tags-container">
                            <span className="tag type primary">Ìä∏Îûô</span>
                            <span className="tag type secondary">ÎèÑÏã¨</span>
                        </div>
                    </div>
                </div>

                {/* ÏΩîÏä§ ÏÑ§Î™Ö */}
                <div className="course-section">
                    <div className="section-header">
                        <h3 className="section-title">ÏΩîÏä§ ÏÑ§Î™Ö</h3>
                    </div>
                    <div className="section-content">
                        <p className="course-description">
                            {course.description ||
                                "ÏÑ∏Ï¢ÖÏãúÎØºÏ≤¥Ïú°Í¥Ä ÎÇ¥Î∂ÄÏóê ÏúÑÏπòÌïú 400m ÌëúÏ§Ä Ìä∏ÎûôÏúºÎ°ú, Ï¥àÎ≥¥ÏûêÎ∂ÄÌÑ∞ Ï†ÑÎ¨∏ Îü¨ÎÑàÍπåÏßÄ Î™®Îëê Ïù¥Ïö©Ìï† Ïàò ÏûàÎäî ÏïàÏ†ÑÌïòÍ≥† ÏæåÏ†ÅÌïú Îü¨Îãù ÌôòÍ≤ΩÏùÑ Ï†úÍ≥µÌï©ÎãàÎã§. Í≥†Î¨¥ Ïû¨ÏßàÏùò Ìä∏ÎûôÏúºÎ°ú Î¨¥Î¶éÏóê Î∂ÄÎã¥Ïù¥ Ï†ÅÏúºÎ©∞, ÏïºÍ∞ÑÏóêÎèÑ Ï°∞Î™ÖÏù¥ Ïûò ÎêòÏñ¥ ÏûàÏñ¥ ÏïàÏ†ÑÌïú Îü¨ÎãùÏù¥ Í∞ÄÎä•Ìï©ÎãàÎã§."}
                        </p>
                    </div>
                </div>

                {/* ÏûëÏÑ±Ïûê Ï†ïÎ≥¥ */}
                <div className="course-section">
                    <div className="section-content">
                        <div className="author-info">
                            <span className="author-label">
                                ÏûëÏÑ±Ïûê : ÏÜêÌù•ÎØº
                            </span>
                        </div>
                    </div>
                </div>

                {/* ÌïòÎã® Î≤ÑÌäº */}
                <div className="modal-actions">
                    <button
                        className="action-button primary"
                        onClick={handleFavoriteClick}
                    >
                        ‚ù§Ô∏è Ï¢ãÏïÑÏöî
                    </button>
                    <button
                        className="action-button secondary"
                        onClick={handleViewMapClick}
                    >
                        üìç ÏßÄÎèÑÏóêÏÑú Î≥¥Í∏∞
                    </button>
                </div>
            </div>

            {/* Ïù¥ÎØ∏ÏßÄ Ïä¨ÎùºÏù¥Îçî Î™®Îã¨ */}
            {showImageSlider && (
                <div className="image-slider-modal">
                    <ImageSlider
                        images={courseImages}
                        onClose={() => setShowImageSlider(false)}
                    />
                </div>
            )}
        </div>
    );
};

export default CourseDetailModal;
